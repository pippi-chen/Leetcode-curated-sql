DROP TABLE BOOKS;
CREATE TABLE BOOKS(
 BOOK_ID INT,
 NAME VARCHAR(50),
 AVAILABLE_FROM DATE, 
 PRIMARY KEY (BOOK_ID)
);

DROP TABLE ORDERS;
CREATE TABLE ORDERS(
 ORDER_ID INT,
 BOOK_ID INT,
 QUANTITY INT,
 DISPATCH_DATE DATE,
 PRIMARY KEY (ORDER_ID)
);

